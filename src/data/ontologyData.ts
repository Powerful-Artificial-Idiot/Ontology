function property(
  name: string,
  dataType: OntologyProperty["dataType"],
  description: string,
  options: Partial<Omit<OntologyProperty, "id" | "name" | "label" | "dataType" | "description">> = {},
): OntologyProperty {
  return {
    id: `prop-${name}`,
    name,
    label: humanize(name),
    dataType,
    description,
    ...options,
  };
}

function objectType(
  id: string,
  label: string,
  domain: OntologyDomain,
  description: string,
  sourceSystems: string[],
  properties: OntologyProperty[],
  examples: string[],
  badges: string[] = ["Source-mapped"],
): OntologyObjectType {
  const existingPropertyNames = new Set(properties.map((item) => item.name));
  return {
    id,
    label,
    domain,
    description,
    sourceSystems,
    properties: [
      ...properties,
      ...sourceMappingProperties.filter((item) => !existingPropertyNames.has(item.name)),
    ],
    examples,
    status: "active",
    badges,
  };
}

function linkType(
  id: string,
  label: string,
  sourceObjectType: string,
  targetObjectType: string,
  cardinality: OntologyLinkType["cardinality"],
  domain: OntologyDomain,
  description: string,
  properties: OntologyProperty[] = [],
  examples: string[] = [],
): OntologyLinkType {
  return {
    id,
    label,
    sourceObjectType,
    targetObjectType,
    cardinality,
    domain,
    description,
    properties,
    examples,
  };
}

